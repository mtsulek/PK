L=imread('kurak.jpg');
figure;
imshow(L);
L1=rgb2gray(L);
figure;
imshow(L1);
figure;
imhist(L1);
[y,x]=imhist(L1);
L2=imadjust(L1,[0 1],[150/255 220/255]);
figure;
imshow(L2);
figure;
imhist(L2);
[y,x]=imhist(L2);
x=(1:256)'/256;
y=(1:128)'/128;
z=(1:64)'/64;
x1=ones(64,1);
r=[0.5*z;0.5*x1;1-y];
g=[z;1-z;x1*0.5;0.5-0.5*z];
b=[1-z;z;z;1-z];
mapa=[r g b];
figure;
imshow(L2,'Colormap',mapa);
figure;
rgbplot(mapa);